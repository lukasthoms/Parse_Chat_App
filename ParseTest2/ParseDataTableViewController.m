#import "ParseDataTableViewController.h"

@interface ParseDataTableViewController ()

@property (strong, nonatomic) NSArray *parseStrings;

@end

@implementation ParseDataTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    PFQuery *query = [PFQuery queryWithClassName:@"TestObject"];
    NSArray *queryResults = [query findObjects];
    NSMutableArray *stringHolder = [@[] mutableCopy];
    for (PFObject *object in queryResults) {
        NSString *parseString = [object objectForKey:@"foo"];
        [stringHolder addObject:parseString];
    }
    self.parseStrings = [NSArray arrayWithArray:stringHolder];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    // Return the number of rows in the section.
    return self.parseStrings.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"parseCell" forIndexPath:indexPath];
    
    cell.textLabel.text = self.parseStrings[indexPath.row];
    
    return cell;
}


